package Java_Tutorial;

public class java_Operators {
	public static void main(String[] args) {
		int x = 10;
		int y = 5;

		// Toán tử số học
		System.out.println(x + y);
		System.out.println(x % y);

		// Toán tử so sánh
		System.out.println(x > y);

		// Toán tử gán

		x += 3;
		System.out.println(x);

		// Toán tử logic

		System.out.println(x > y && x < 20);
	}

}
